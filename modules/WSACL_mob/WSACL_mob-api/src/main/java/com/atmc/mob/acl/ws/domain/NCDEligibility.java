package com.atmc.mob.acl.ws.domain;

public class NCDEligibility {
	
	private String statusCode = "";
	private String NCDReference = "";
	private String NCDFreeYears = "";
	private String errorCode = "";
	private String errorMsg = "";
	/**
	 * @return the statusCode
	 */
	public String getStatusCode() {
		return statusCode;
	}
	/**
	 * @param statusCode the statusCode to set
	 */
	public void setStatusCode(String statusCode) {
		this.statusCode = statusCode;
	}
	/**
	 * @return the nCDReference
	 */
	public String getNCDReference() {
		return NCDReference;
	}
	/**
	 * @param nCDReference the nCDReference to set
	 */
	public void setNCDReference(String nCDReference) {
		NCDReference = nCDReference;
	}
	/**
	 * @return the nCDFreeYears
	 */
	public String getNCDFreeYears() {
		return NCDFreeYears;
	}
	/**
	 * @param nCDFreeYears the nCDFreeYears to set
	 */
	public void setNCDFreeYears(String nCDFreeYears) {
		NCDFreeYears = nCDFreeYears;
	}
	/**
	 * @return the errorCode
	 */
	public String getErrorCode() {
		return errorCode;
	}
	/**
	 * @param errorCode the errorCode to set
	 */
	public void setErrorCode(String errorCode) {
		this.errorCode = errorCode;
	}
	/**
	 * @return the errorMsg
	 */
	public String getErrorMsg() {
		return errorMsg;
	}
	/**
	 * @param errorMsg the errorMsg to set
	 */
	public void setErrorMsg(String errorMsg) {
		this.errorMsg = errorMsg;
	}
	
	

}
