package com.atmc.mob.acl.ws.domain.yakeen;

/**
 * @author Basel
 *
 */
public class AddressInfo extends ServiceResponse {
    private String additionalNumber;
    private String buildingNumber;
    private String city;
    private String district;
    private String postCode;
    private String streetName;
    private String isPrimaryAddress;
	/**
	 * @return the additionalNumber
	 */
	public String getAdditionalNumber() {
		return additionalNumber;
	}
	/**
	 * @param additionalNumber the additionalNumber to set
	 */
	public void setAdditionalNumber(String additionalNumber) {
		this.additionalNumber = additionalNumber;
	}
	/**
	 * @return the buildingNumber
	 */
	public String getBuildingNumber() {
		return buildingNumber;
	}
	/**
	 * @param buildingNumber the buildingNumber to set
	 */
	public void setBuildingNumber(String buildingNumber) {
		this.buildingNumber = buildingNumber;
	}
	/**
	 * @return the city
	 */
	public String getCity() {
		return city;
	}
	/**
	 * @param city the city to set
	 */
	public void setCity(String city) {
		this.city = city;
	}
	/**
	 * @return the district
	 */
	public String getDistrict() {
		return district;
	}
	/**
	 * @param district the district to set
	 */
	public void setDistrict(String district) {
		this.district = district;
	}
	/**
	 * @return the postCode
	 */
	public String getPostCode() {
		return postCode;
	}
	/**
	 * @param postCode the postCode to set
	 */
	public void setPostCode(String postCode) {
		this.postCode = postCode;
	}
	/**
	 * @return the streetName
	 */
	public String getStreetName() {
		return streetName;
	}
	/**
	 * @param streetName the streetName to set
	 */
	public void setStreetName(String streetName) {
		this.streetName = streetName;
	}
	/**
	 * @return the isPrimaryAddress
	 */
	public String getIsPrimaryAddress() {
		return isPrimaryAddress;
	}
	/**
	 * @param isPrimaryAddress the isPrimaryAddress to set
	 */
	public void setIsPrimaryAddress(String isPrimaryAddress) {
		this.isPrimaryAddress = isPrimaryAddress;
	}
    
    
    
}
